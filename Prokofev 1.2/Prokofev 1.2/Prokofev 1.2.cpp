#include <iostream>
#include <string>

int main() 
{
    setlocale(LC_ALL, "");

    // Создаем массив с расписанием на неделю
    std::string schedule[7] = {
        "Понедельник: Уроки с 9:00 до 15:00",
        "Вторник: Утренняя зарядка с 7:00 до 7:30",
        "Среда: Поход в библиотеку с 10:00 до 11:30",
        "Четверг: Спортивные занятия с 14:00 до 16:00",
        "Пятница: Встреча с друзьями с 17:00 до 20:00",
        "Суббота: Выходной",
        "Воскресенье: Посещение родственников с 12:00 до 17:00"
    };

    // Запрашиваем у пользователя день недели
    std::cout << "Введите день недели (1 - Понедельник, 2 - Вторник, и так далее): ";
    int day;
    std::cin >> day;

    // Проверяем корректность ввода и выводим расписание
    if (day >= 1 && day <= 7) {
        std::cout << "Расписание на " << schedule[day - 1] << std::endl;
    }
    else {
        std::cout << "Некорректный ввод. Введите число от 1 до 7." << std::endl;
    }

    return 0;
}